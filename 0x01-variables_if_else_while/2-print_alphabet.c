#include <stdio.h>
#include <stdlib.h>

/**
* main - Entry point
* Description: A C program to identify last digit of a given number and compare
* Return: 0 if successful
*/

int main(void)
{
	char lcase;

	for (lcase = 'a'; lcase <= 'z'; lcase++)
	{
		putchar(lcase);
		putchar('\n');
	}
	return (0);
}
